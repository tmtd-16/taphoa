// Chi tiết sản phẩm Socola
document.getElementById("showDetailsSocola").addEventListener("click", function(event) {
    event.preventDefault();
    document.getElementById("productDetailsSocola").style.display = "block";
  });
  
  document.getElementById("hideDetailsSocola").addEventListener("click", function(event) {
    event.preventDefault();
    document.getElementById("productDetailsSocola").style.display = "none";
  });
  
